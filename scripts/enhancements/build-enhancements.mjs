// Generates a Supabase seed file of lessons.enhancements UPDATEs from JS data,
// so the JSON is always valid (no hand-escaping). Each module file default-exports
// { courseSlug, moduleNumber, lessons: [{ lessonNumber, enhancements }] }.
//
// Usage: node scripts/enhancements/build-enhancements.mjs 90day-module1
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..', '..');

const name = process.argv[2];
if (!name) { console.error('Usage: build-enhancements.mjs <module-data-file>'); process.exit(1); }

const data = (await import(`./${name}.mjs`)).default;
const { courseSlug, moduleNumber, lessons } = data;

const DELIM = '$ench$'; // dollar-quote delimiter; content never contains this
let sql = `-- ============================================================
-- Lesson enhancements for "${courseSlug}" Module ${moduleNumber}.
-- Generated by scripts/enhancements/build-enhancements.mjs — do not hand-edit;
-- edit scripts/enhancements/${name}.mjs and regenerate. Run AFTER
-- lesson-enhancements-schema.sql and the lesson seeds. Idempotent.
-- ============================================================\n\n`;

for (const { lessonNumber, enhancements } of lessons) {
  const json = JSON.stringify(enhancements);
  if (json.includes(DELIM)) throw new Error(`Delimiter clash in lesson ${lessonNumber}`);
  sql += `update public.lessons l set enhancements = ${DELIM}${json}${DELIM}::jsonb
from public.modules m join public.courses c on c.id = m.course_id
where l.module_id = m.id and c.slug = '${courseSlug}'
  and m.module_number = ${moduleNumber} and l.lesson_number = ${lessonNumber};\n\n`;
}

const out = join(ROOT, 'supabase', `seed-90day-module${moduleNumber}-enhancements.sql`);
writeFileSync(out, sql);
console.log(`Wrote ${out} (${lessons.length} lessons)`);
